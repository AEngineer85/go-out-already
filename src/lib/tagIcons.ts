/**
 * Returns the visual config for the first tag that has a definition,
 * falling back to the default calendar style.
 */
export function getEventVisual(tags: string[]): TagVisual {
  for (const tag of tags) {
    if (TAG_VISUALS[tag]) return TAG_VISUALS[tag];
  }
  return DEFAULT_VISUAL;
}
